#include <stdio.h>

int main() {
    int a = 10, b = 5;
    printf("Gia tri cua a + b la: %d \n", a + b);
    printf("Gia tri cua a - b la: %d \n", a - b);
    printf("Gia tri cua a * b la: %d \n", a * b);
    printf("Gia tri cua a / b la: %d \n", a / b);
    printf("Gia tri cua a %% b la: %d \n", a % b);
    return 0;
}